package com.example.doltmar11.Model;

public class ToDoModel {
    private int id, sratus;
    private String task;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getSratus() {
        return sratus;
    }

    public void setSratus(int sratus) {
        this.sratus = sratus;
    }

    public String getTask() {
        return task;
    }

    public void setTask(String task) {
        this.task = task;
    }
}
